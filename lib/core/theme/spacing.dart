/// AMOZIT Customer App Spacing Tokens
/// 
/// All spacing values are extracted from Figma design system.
/// These spacing constants should be used for consistent layout spacing.
class AppSpacing {
  AppSpacing._();

  // Base spacing unit (2dp)
  static const double base = 2.0;

  // Spacing scale
  /// Extra small spacing - 2dp
  static const double xs = 2.0;

  /// Small spacing - 4dp
  static const double sm = 4.0;

  /// Medium spacing - 8dp
  static const double md = 8.0;

  /// Large spacing - 16dp
  static const double lg = 16.0;

  /// Extra large spacing - 20dp
  static const double xl = 20.0;

  /// 2X large spacing - 24dp
  static const double xxl = 24.0;

  /// 3X large spacing - 28dp
  static const double xxxl = 28.0;

  /// 4X large spacing - 32dp
  static const double xxxxl = 32.0;

  /// 5X large spacing - 48dp
  static const double xxxxxl = 48.0;

  /// 6X large spacing - 64dp
  static const double xxxxxxl = 64.0;
}

/// Border radius tokens from Figma design system
class AppBorderRadius {
  AppBorderRadius._();

  /// Small border radius - 8dp
  static const double sm = 8.0;

  /// Large border radius - 28dp (used for buttons)
  static const double lg = 28.0;

  /// Extra large border radius - 32dp
  static const double xl = 32.0;
}

